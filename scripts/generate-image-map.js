#!/usr/bin/env node
/**
 * generate-image-map.js
 *
 * Scans assets/images/ for PNG/JPG files and generates lib/plantImageMap.ts.
 *
 * Naming convention:
 *   {plant_id}.png        → card image
 *   {plant_id}_hero.png   → modal hero (optional, falls back to card)
 *
 * Run manually:  node scripts/generate-image-map.js
 * Auto-runs via: npm start  (see package.json prestart)
 */

const fs   = require('fs');
const path = require('path');

const IMAGES_DIR = path.join(__dirname, '..', 'assets', 'images');
const OUT_FILE   = path.join(__dirname, '..', 'lib', 'plantImageMap.ts');

const IMAGE_EXTS = ['.png', '.jpg', '.jpeg', '.webp'];

// Collect all image files
const files = fs.readdirSync(IMAGES_DIR).filter(f =>
  IMAGE_EXTS.includes(path.extname(f).toLowerCase())
);

// Separate hero images from card images
const heroFiles = new Set(
  files
    .filter(f => path.basename(f, path.extname(f)).endsWith('_hero'))
    .map(f => path.basename(f, path.extname(f)).replace(/_hero$/, ''))
);

const cardFiles = files.filter(f => !path.basename(f, path.extname(f)).endsWith('_hero'));

// Build entries
const entries = cardFiles.map(file => {
  const ext    = path.extname(file);
  const id     = path.basename(file, ext);
  const hasHero = heroFiles.has(id);

  // Find the hero filename
  const heroFile = hasHero
    ? files.find(f => path.basename(f, path.extname(f)) === `${id}_hero`)
    : null;

  const cardReq = `require('../assets/images/${file}')`;
  const heroReq = heroFile ? `, hero: require('../assets/images/${heroFile}')` : '';

  return `  ${JSON.stringify(id)}: { card: ${cardReq}${heroReq} },`;
});

const output = `// ─── Auto-generated by scripts/generate-image-map.js ────────────────────────
// Do not edit manually. Add images to assets/images/ then run:
//   node scripts/generate-image-map.js
//
// Naming convention:
//   {plant_id}.png        → card thumbnail
//   {plant_id}_hero.png   → full-width modal hero (optional)
//
// Generated: ${new Date().toISOString()}

export const PLANT_IMAGES: Record<string, { card: any; hero?: any }> = {
${entries.join('\n')}
};
`;

fs.writeFileSync(OUT_FILE, output, 'utf-8');

console.log(`✓ plantImageMap.ts updated — ${cardFiles.length} card image${cardFiles.length !== 1 ? 's' : ''} (${heroFiles.size} with hero)`);
cardFiles.forEach(f => {
  const id = path.basename(f, path.extname(f));
  console.log(`  ${heroFiles.has(id) ? '📸+hero' : '📷      '} ${id}`);
});
